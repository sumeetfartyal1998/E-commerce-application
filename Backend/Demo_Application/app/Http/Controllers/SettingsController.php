    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $settings=Settings::find($id);
        return view('settings')->with('settings',$settings);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $req, $id)
    {
        $settings=Settings::find($id);
        if(!$req->userRegistered){
            $settings->userRegistered=0;
        }else{
            $settings->userRegistered=1;
        }

        if(!$req->orderPlaced){
            $settings->orderPlaced=0;
        }else{
            $settings->orderPlaced=1;
        }

        if(!$req->contactUs){
            $settings->contactUs=0;
        }else{
            $settings->contactUs=1;
        }
        
        if($settings->save()){
            return back()->with('success','Your changes has been saved');
        }
    }

